# with append() method - add single element at the end
my_list = [1, 2, 3]
my_list.append(4)
print(my_list)

# with extend() method, add multiple elements at the end
my_list_two = [1, 2, 3]
my_list_two.extend([4, 5])
print(my_list_two)

# with insert() method - add single element at a specific index

my_list_three = [1, 2, 3]
my_list_three.insert(1, 4)
print(my_list_three)

